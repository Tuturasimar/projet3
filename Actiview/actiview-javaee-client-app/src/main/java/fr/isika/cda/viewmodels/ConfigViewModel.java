package fr.isika.cda.viewmodels;

public class ConfigViewModel {
	
	private Long coloConfigId;
	private Long fontConfigId;
	private Long imageConfiId;
	private Long templateConfigId;
	
	public Long getColoConfigId() {
		return coloConfigId;
	}
	public void setColoConfigId(Long coloConfigId) {
		this.coloConfigId = coloConfigId;
	}
	public Long getFontConfigId() {
		return fontConfigId;
	}
	public void setFontConfigId(Long fontConfigId) {
		this.fontConfigId = fontConfigId;
	}
	public Long getImageConfiId() {
		return imageConfiId;
	}
	public void setImageConfiId(Long imageConfiId) {
		this.imageConfiId = imageConfiId;
	}
	public Long getTemplateConfigId() {
		return templateConfigId;
	}
	public void setTemplateConfigId(Long templateConfigId) {
		this.templateConfigId = templateConfigId;
	}
	
	

}
